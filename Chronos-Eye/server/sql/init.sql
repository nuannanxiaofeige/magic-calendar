-- 时光之眼数据库初始化脚本
-- 创建数据库
CREATE DATABASE IF NOT EXISTS chronos_eye DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE chronos_eye;

-- 关闭外键检查，允许删除有外键约束的表
SET FOREIGN_KEY_CHECKS = 0;

-- ============================================
-- 用户表
-- ============================================
DROP TABLE IF EXISTS `users`;
CREATE TABLE `users` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `openid` VARCHAR(64) UNIQUE NOT NULL COMMENT '微信小程序 openid',
  `unionid` VARCHAR(64) COMMENT '微信 unionid',
  `nickname` VARCHAR(64) COMMENT '昵称',
  `avatar` VARCHAR(255) COMMENT '头像 URL',
  `phone` VARCHAR(20) COMMENT '手机号',
  `gender` TINYINT DEFAULT 0 COMMENT '性别 0-未知 1-男 2-女',
  `birthday` DATE COMMENT '生日',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX `idx_openid` (`openid`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- ============================================
-- 节假日表
-- ============================================
DROP TABLE IF EXISTS `holidays`;
CREATE TABLE `holidays` (
  `id` INT PRIMARY KEY AUTO_INCREMENT COMMENT '主键 ID',
  `name` VARCHAR(100) NOT NULL COMMENT '节日名称',
  `type` ENUM('festival', 'solar', 'lunar', 'term') NOT NULL DEFAULT 'solar' COMMENT '类型：festival-法定节假日，solar-公历节日，lunar-农历节日，term-节气',

  -- 日期信息
  `date_full` DATE COMMENT '完整日期 (用于具体某一天)',
  `date_month` INT COMMENT '月份',
  `date_day` INT COMMENT '日期',
  `weekday` INT DEFAULT 0 COMMENT '星期几 (0-周日，1-周一...)',

  -- 农历信息
  `lunar_month` INT COMMENT '农历月份',
  `lunar_day` INT COMMENT '农历日期',
  `is_leap` TINYINT DEFAULT 0 COMMENT '是否闰月',

  -- 假期详情 - 天行 API 字段
  `vacation_dates` TEXT COMMENT '放假日期列表，|分隔',
  `work_dates` TEXT COMMENT '调休上班日期列表，|分隔',
  `wage_dates` TEXT COMMENT '三倍工资日期列表，|分隔',

  -- 假期属性
  `is_official` TINYINT DEFAULT 0 COMMENT '是否法定节假日',
  `is_rest` TINYINT DEFAULT 1 COMMENT '是否休息日 (1-休息，0-上班)',
  `is_work` TINYINT DEFAULT 0 COMMENT '是否调休上班 (1-调休上班)',
  `duration` INT DEFAULT 1 COMMENT '假期持续天数',

  -- 描述信息
  `tip` TEXT COMMENT '放假调休说明',
  `rest_tip` TEXT COMMENT '请假建议 (天行 API 的 rest 字段)',
  `description` TEXT COMMENT '节日描述',
  `customs` TEXT COMMENT '习俗',

  -- 天行 API 原始数据
  `holiday_str` VARCHAR(50) COMMENT '天行 API 返回的 holiday 字段 (如"1 月 1 号")',
  `start` INT DEFAULT 0 COMMENT '假期开始偏移',
  `end` INT DEFAULT 0 COMMENT '假期结束偏移',
  `now` INT DEFAULT 0 COMMENT '当前状态',

  -- 管理字段
  `year` INT COMMENT '年份',
  `is_active` TINYINT DEFAULT 1 COMMENT '是否启用',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

  INDEX `idx_date_full` (`date_full`),
  INDEX `idx_year` (`year`),
  INDEX `idx_type` (`type`),
  INDEX `idx_month_day` (`date_month`, `date_day`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='节假日表';

-- ============================================
-- 节气日期表 - 用于存储每年节气的具体日期
-- ============================================
DROP TABLE IF EXISTS `almanac_term_dates`;
CREATE TABLE `almanac_term_dates` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `year` INT NOT NULL COMMENT '年份',
  `term_name` VARCHAR(50) NOT NULL COMMENT '节气名称',
  `term_order` INT NOT NULL COMMENT '节气序号 (1-24)',
  `date` DATE NOT NULL COMMENT '交节日期',
  `time` VARCHAR(10) DEFAULT '00:00' COMMENT '交节时间',
  `week` INT DEFAULT 0 COMMENT '星期几',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY `uk_year_term` (`year`, `term_name`),
  INDEX `idx_date` (`date`),
  INDEX `idx_year` (`year`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='二十四节气日期表';

-- ============================================
-- 用户倒计时表（用户关注的节日）
-- ============================================
DROP TABLE IF EXISTS `user_countdowns`;
CREATE TABLE `user_countdowns` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `user_id` INT NOT NULL COMMENT '用户 ID',
  `holiday_id` INT COMMENT '节日 ID（系统节日）',
  `custom_name` VARCHAR(64) COMMENT '自定义名称（用于自定义节日）',
  `custom_date` DATE COMMENT '自定义日期',
  `custom_type` ENUM('solar', 'lunar') DEFAULT 'solar' COMMENT '自定义类型',
  `custom_lunar_month` INT COMMENT '自定义农历月份',
  `custom_lunar_day` INT COMMENT '自定义农历日期',
  `is_recurring` TINYINT DEFAULT 0 COMMENT '是否循环（每年提醒）',
  `reminder_days` INT DEFAULT 1 COMMENT '提前几天提醒',
  `is_enabled` TINYINT DEFAULT 1 COMMENT '是否启用',
  `sort_order` INT DEFAULT 0 COMMENT '排序顺序',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
  FOREIGN KEY (`holiday_id`) REFERENCES `holidays`(`id`) ON DELETE SET NULL,
  INDEX `idx_user` (`user_id`),
  INDEX `idx_enabled` (`is_enabled`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户倒计时表';

-- ============================================
-- 日程表
-- ============================================
DROP TABLE IF EXISTS `schedules`;
CREATE TABLE `schedules` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `user_id` INT NOT NULL COMMENT '用户 ID',
  `title` VARCHAR(128) NOT NULL COMMENT '日程标题',
  `description` TEXT COMMENT '日程描述',
  `start_time` DATETIME NOT NULL COMMENT '开始时间',
  `end_time` DATETIME COMMENT '结束时间',
  `is_all_day` TINYINT DEFAULT 0 COMMENT '是否全天',
  `repeat_type` ENUM('none', 'daily', 'weekly', 'monthly', 'yearly') DEFAULT 'none' COMMENT '重复类型',
  `repeat_end` DATE COMMENT '重复结束日期',
  `reminder_enabled` TINYINT DEFAULT 1 COMMENT '是否启用提醒',
  `reminder_minutes` INT DEFAULT 30 COMMENT '提前几分钟提醒',
  `priority` ENUM('low', 'normal', 'high') DEFAULT 'normal' COMMENT '优先级',
  `status` ENUM('pending', 'completed', 'cancelled') DEFAULT 'pending' COMMENT '状态',
  `completed_at` DATETIME COMMENT '完成时间',
  `color` VARCHAR(20) DEFAULT '#667eea' COMMENT '日程颜色',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
  INDEX `idx_user_time` (`user_id`, `start_time`),
  INDEX `idx_status` (`status`),
  INDEX `idx_priority` (`priority`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='日程表';

-- ============================================
-- 历史事件表
-- ============================================
DROP TABLE IF EXISTS `history_events`;
CREATE TABLE `history_events` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `month` INT NOT NULL COMMENT '月份',
  `day` INT NOT NULL COMMENT '日期',
  `year` INT NOT NULL COMMENT '年份',
  `title` VARCHAR(255) NOT NULL COMMENT '事件标题',
  `description` TEXT COMMENT '事件描述',
  `category` ENUM('politics', 'military', 'science', 'culture', 'sports', 'entertainment', 'other') DEFAULT 'other' COMMENT '事件分类',
  `country` VARCHAR(64) COMMENT '相关国家',
  `is_featured` TINYINT DEFAULT 0 COMMENT '是否精选',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  INDEX `idx_date` (`month`, `day`),
  INDEX `idx_year` (`year`),
  INDEX `idx_category` (`category`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='历史事件表';

