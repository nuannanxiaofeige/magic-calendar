-- ============================================
-- 二十四节气数据表 - 详细节气百科
-- ============================================
DROP TABLE IF EXISTS `almanac_terms`;
CREATE TABLE `almanac_terms` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `term_name` VARCHAR(50) NOT NULL COMMENT '节气名称',
  `term_order` INT NOT NULL COMMENT '节气序号 (1-24)',
  `lunar_month` INT COMMENT '大致农历月份',
  `lunar_day` INT COMMENT '大致农历日期',
  `origin` TEXT COMMENT '节气起源/由来',
  `phenology` TEXT COMMENT '物候特征',
  `customs` TEXT COMMENT '传统习俗',
  `health_tips` TEXT COMMENT '养生建议',
  `poetry` VARCHAR(255) COMMENT '相关诗词',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY `uk_term_name` (`term_name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='二十四节气百科表';

-- ============================================
-- 节气交节时间表 - 每年精确到分钟
-- ============================================
DROP TABLE IF EXISTS `almanac_term_dates`;
CREATE TABLE `almanac_term_dates` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `year` INT NOT NULL COMMENT '年份',
  `term_name` VARCHAR(50) NOT NULL COMMENT '节气名称',
  `term_order` INT NOT NULL COMMENT '节气序号',
  `term_time` DATETIME NOT NULL COMMENT '交节时间 (精确到分钟)',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY `uk_year_term` (`year`, `term_name`),
  INDEX `idx_term_time` (`term_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='节气交节时间表';

